from flask import Flask, render_template_string
import matplotlib.pyplot as plt
import mpld3

app = Flask(__name__)

# Generate the first plot
def generate_plot():
    x = [1, 2, 3, 4, 5]
    y = [10, 20, 25, 30, 35]
    plt.figure(figsize=(8, 6))
    plt.plot(x, y, marker='o', linestyle='-', color='b', label='Sample Line')
    plt.title('Sample Plot')
    plt.xlabel('X-axis')
    plt.ylabel('Y-axis')
    plt.legend()
    plt.grid(True)
    return mpld3.fig_to_html(plt.gcf())

# Flask endpoint for the first plot
@app.route('/plot')
def plot():
    plot_html = generate_plot()
    return render_template_string(plot_html)

# Flask endpoint for the additional plot
@app.route('/additional_plot')
def additional_plot():
    plot_html = generate_plot()  # You can modify this to generate a different plot
    return render_template_string(plot_html)

if __name__ == '__main__':
    app.run(debug=True)
